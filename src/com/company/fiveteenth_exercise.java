package com.company;

public class fiveteenth_exercise {
    void multilanguage(String nyelv) {
        if (nyelv.equals("English")) {
            System.out.println("Hi!");
        } else if (nyelv.equals("Magyar")) {
            System.out.println("Szia!");
        } else if (nyelv.equals("Spanish")) {
            System.out.println("Hola!");
        } else if (nyelv.equals("Deutsch")) {
            System.out.println("Servus!");
        } else if (nyelv.equals("Polish")) {
            System.out.println("Cześć!");
        } else if (nyelv.equals("None")) {
            System.out.println("♥");
        }
    }
}
